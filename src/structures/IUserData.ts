/**
 * Represents basic user data
 * Contains the id and the name of the user
 *
 * @export
 * @interface IUserData
 */
export interface IUserData {
    /**
     * The id of the user
     *
     * @type {string}
     * @memberOf IUserData
     */
    id: string;

    /**
     * The name of the user
     *
     * @type {string}
     * @memberof IUserData
     */
    name: string;
}
